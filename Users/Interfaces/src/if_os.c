uint8 if_pwr_get_batt_percent() {
	uint32 conval;
	uint16 sigma = 0;
	uint8 i;
	if(HAL_ADC_Start(&hadc1) == HAL_OK) {
		HAL_ADC_PollForConversion(&hadc1, 3200);
		conval = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);
	}
	g_arr_batt[g_index_batt++] = conval;
	if(g_index_batt == 4) g_index_batt = 0;
	if(g_total_batt < 4) { g_total_batt++; }
	for(i=0;i<g_total_batt;i++) sigma += g_arr_batt[i];
	conval = sigma/g_total_batt;
#ifdef STM32F7
	//VBAT = ((ADC18_value * 4) / 0x10000) * VREF
	if(conval < 0x39c) return 0;
	if(conval > 0x530) return 100;
	return ((conval - 0x39c) * 100) / 0x194;

#endif
#ifdef STM32F4	
#ifdef STM32F427xx
	if(conval < 0x300) return 0;
	if(conval > 0x500) return 100;
	return ((conval - 0x300) * 100) / 0x200;	
#else
	if(conval < 0x700) return 0;
	if(conval > 0x900) return 100;
	return ((conval - 0x700) * 100) / 0x200;
#endif
#endif
}

