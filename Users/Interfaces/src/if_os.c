//transmit tcp request and wait for response
uint32 if_download_update(net_context_p ctx, uint8 * url, uint32 * address, uint8 * sign) {
	uint16 plen;
	uint32 len;
	uint8 host[210];
	uint8 cbuf[512];
	uint32 i = 0;
	FLASH_EraseInitTypeDef FLASH_EraseInitStructure;
	uint32 sector_error;
	uint32 base_addr;
	uint32 timeout = (800000 * 1000);
	uint8 * ptr;
	uint8 c;
	uint8 state;
	uint8 hdr[256];
	uint8 ihdr = 0;
	uint16 port = 80;
	uint32 content_length = 0;
	uint32 data_length = 0;
	uint8 line_cntr = 0;
	bfd_info b_info;
	net_conn_p conn;
#if SHARD_RTOS_ENABLED
	uint32 cpu_sr;
#endif
	uint8 b_len;
	len = 0;
	if((ctx->state & IF_NET_STATE_CONNECTED) == 0) return 0;		//not connected
#if SHARD_RTOS_ENABLED
	cpu_sr = os_enter_critical();
#endif
	NET_LOCK_CRITICAL(ctx);
	//start decode URL
	ptr = net_decode_url(url, &port, host);
	sprintf((char *)cbuf, "GET %s HTTP/1.1\r\nHost: %s\r\n\r\n", ptr, host);
	//connecting to Host
	if((conn = ctx->sock_open(ctx, host, port, NET_TRANSMIT)) == NULL)  {
		//error connecting to server
		NET_UNLOCK_CRITICAL(ctx);
#if SHARD_RTOS_ENABLED
		os_exit_critical(cpu_sr);
#endif
		return 0;
	}
	//flash unlock
	HAL_FLASH_Unlock();
#ifdef STM32F407xx
	__HAL_FLASH_CLEAR_FLAG(FLASH_FLAG_EOP | FLASH_FLAG_OPERR | FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR | FLASH_FLAG_PGPERR | FLASH_FLAG_PGSERR);
#endif
#ifdef STM32F746xx
	__HAL_FLASH_CLEAR_FLAG(FLASH_FLAG_EOP | FLASH_FLAG_OPERR | FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR | FLASH_FLAG_PGPERR | FLASH_FLAG_ERSERR);
#endif
	//start erasing flash
	FLASH_EraseInitStructure.Sector = 8;
	FLASH_EraseInitStructure.TypeErase = FLASH_TYPEERASE_SECTORS;
	FLASH_EraseInitStructure.VoltageRange = FLASH_VOLTAGE_RANGE_3;
	//FLASH_EraseInitStructure.Banks = FLASH_BANK_1;
	FLASH_EraseInitStructure.NbSectors = 4;
	HAL_FLASHEx_Erase(&FLASH_EraseInitStructure, &sector_error);
	//if(sector_error != 0xFFFFFFFFU) goto close_connection;
	//start transmit HTTP request
	if_net_tcp_send(conn, cbuf, strlen((const char *)cbuf));
	len = 0;
	base_addr = 0x08080000 + sizeof(bfd_info);		//temporary area
	address[0] = base_addr;
	if(if_net_tcp_accept(conn) == 0) {
		while(1) {
			for(i=0;i<timeout;i++) { if(__HAL_USART_GET_FLAG((USART_HandleTypeDef *)ctx, USART_FLAG_RXNE) != RESET) break; }
			if(i == timeout) goto close_connection;
			for(i=0;i<20000;i++) {  
				if(__HAL_USART_GET_FLAG((USART_HandleTypeDef *)ctx, USART_FLAG_RXNE) != SET) continue;
				i = 0;
#if SHARD_WIFI_MODULE == MOD_ESP8266
				switch(state) {
					default:
					case 0:
						hdr[ihdr++] = ctx->handle.Instance->DR;
						hdr[ihdr] = 0;
						if((ptr = (uint8 *)strstr((char *)hdr, "+IPD,")) != NULL) {
							state = 1;
						} 
						else if(strstr((char *)hdr, "SEND FAIL") != NULL) { len = 0; goto close_connection; }
						else if(strstr((char *)hdr, "ERROR") != NULL) { len = 0; goto close_connection;  }				//link not valid
						else if(strstr((char *)hdr, "CLOSED") != NULL) goto close_connection; 
						break;
					case 1:		//reset index header
						ihdr = 0;
						state = 2;		//wait for length
					case 2:
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						if(c == ',') ihdr = 0;		//found comma, reset header
						if(c == ':') { 
							hdr[ihdr] = 0;
							plen = atoi((const char *)hdr);		//data length fetched
							if(content_length == 0) state = 3;
							else state = 8;
							ihdr = 0;
						}
						break;
					case 3:
						plen--;
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						if(c == '\n') {
							hdr[ihdr] = 0;
							if((ptr = (uint8 *)strstr((char *)hdr, "Content-Length:")) != NULL) {
								content_length = atoi((const char *)ptr + 15);
								state = 4;
							}
							ihdr = 0;
						}
						break;
					case 4:
						plen--;
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						if(c == '\n') {
							hdr[ihdr] = 0;
							if((ptr = (uint8 *)strstr((char *)hdr, "Content-Type:")) != NULL) {
								//content_length = atoi((const char *)ptr + 13);
								state = 5;
							}
							ihdr = 0;
						}
						break;
					case 5:
						plen--;
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						hdr[ihdr] = 0;
						if(c == '\n') {
							if(strlen((const char *)hdr) == 2) state = 8;
							ihdr = 0;
						}
						break;
					case 8:
						c = ctx->handle.Instance->DR;
						HAL_FLASH_Program(FLASH_TYPEPROGRAM_BYTE, (uint32_t)base_addr++, c);
						if(--plen == 0) state = 0;		//start waiting for next frame
						break;
				}
#endif
#if SHARD_WIFI_MODULE == MOD_RTL8710
				switch(state) {
					default:
					case 0:
						hdr[ihdr++] = ctx->handle.Instance->DR;
						hdr[ihdr] = 0;
						c = ctx->handle.Instance->DR;
						if(c == '\n') {
							hdr[ihdr] = 0;
							if((ptr = (uint8 *)strstr((char *)hdr, "Content-Length:")) != NULL) {
								content_length = atoi((const char *)ptr + 15);
								state = 1;
							}
							ihdr = 0;
						}
						break;
					case 1:
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						if(c == '\n') {
							hdr[ihdr] = 0;
							if((ptr = (uint8 *)strstr((char *)hdr, "Content-Type:")) != NULL) {
								//content_length = atoi((const char *)ptr + 13);
								state = 2;
							}
							ihdr = 0;
						}
						break;
					case 2:
						hdr[ihdr++] = c = ctx->handle.Instance->DR;
						hdr[ihdr] = 0;
						if(c == '\n') {
							if(strlen((const char *)hdr) == 2) state = 3;
							ihdr = 0;
						}
						break;
					case 3:
						data_length++;
						c = ctx->handle.Instance->DR;
						HAL_FLASH_Program(FLASH_TYPEPROGRAM_BYTE, (uint32_t)base_addr++, c);
						if(content_length == data_length) goto close_connection;
						break;
				}
#endif
			}
		}
	}
	//close connection
	close_connection:
	//write binary firmware data info struct
	if(if_check_loadfile((uint8 *)0x08080000 + sizeof(bfd_info), sign, content_length)) {
		b_info.status = 0x01;					//start updating firmware during next restart
		b_info.len = content_length;
		memcpy(b_info.signature, sign, IF_KRONOS_SIGNATURE_LEN);
		ptr = (uint8 *)&b_info;
		base_addr = 0x08080000;
		for(b_len=0;b_len<sizeof(bfd_info);b_len++) HAL_FLASH_Program(FLASH_TYPEPROGRAM_BYTE, (uint32_t)base_addr++, ptr[b_len]);
	}
	HAL_FLASH_Lock();
	ctx->sock_close(conn);
	NET_UNLOCK_CRITICAL(ctx);
#if SHARD_RTOS_ENABLED
	os_exit_critical(cpu_sr);
#endif
	return content_length;		//unable to found +IPD prefix
}

uint8 if_check_loadfile(uint8 * offset, uint8 * signature, uint32 length) {
	Sha256 sha_ctx;
	uint8 b_len;
	uint8 sha_chksum[IF_KRONOS_SIGNATURE_LEN];
	
	wc_InitSha256(&sha_ctx);
	wc_Sha256Update(&sha_ctx, (const uint8 *)offset, length);
	wc_Sha256Final(&sha_ctx, sha_chksum);
	return (memcmp(sha_chksum, signature, IF_KRONOS_SIGNATURE_LEN) == 0);
}

