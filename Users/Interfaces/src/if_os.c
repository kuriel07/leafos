uint32 if_time_get(datetime_p value) {
	//return RTC_GetCounter();
	uint8 bcd_second = RTC->TR & 0xFF;
	uint8 bcd_minute = (RTC->TR >> 8) & 0xFF;
	uint8 bcd_hour = (RTC->TR >> 16) & 0x3F;
	uint8 bcd_date = RTC->DR & 0xFF;
	uint8 bcd_month = (RTC->DR >> 8) & 0x1F;
	uint8 bcd_year = (RTC->DR >> 16) & 0xFF;
	uint8 bcd_week = RTC->DR >> 9;
	uint8 second = ((bcd_second >> 4) * 10) + (bcd_second & 0x0F);
	uint8 minute = ((bcd_minute >> 4) * 10) + (bcd_minute & 0x0F);
	uint8 hour = ((bcd_hour >> 4) * 10) + (bcd_hour & 0x0F);
	uint8 date = ((bcd_date >> 4) * 10) + (bcd_date & 0x0F);
	uint8 month = ((bcd_month >> 4) * 10) + (bcd_month & 0x0F);
	uint8 year = ((bcd_year >> 4) * 10) + (bcd_year & 0x0F);
	//if(RTC->TR & (1 << 22)) hour += 12;
	if(hour == 12) hour = 0;
	if(RTC->TR & (1 << 22)) hour += 12;
	value->second = second % 60;		//second
	value->minute = minute % 60;		//minute
	value->hour = hour % 24;			//hour
	value->date = date;					//date
	value->month = month;				//month
	value->year = year + 1970;			//year
	value->tz = RTC->BKP0R;			//timezone
	return 0;
}

