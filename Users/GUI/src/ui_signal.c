static void ui_signal_render(ui_object * obj, gui_handle_p display) {
	OS_DEBUG_ENTRY(ui_signal_render);
	uint16 x = ((ui_rect *)obj)->x;
	uint16 y = ((ui_rect *)obj)->y;
	uint16 w = ((ui_rect *)obj)->w;
	uint16 h = ((ui_rect *)obj)->h;
	static uint8 blink = 0;
	uint16 yy, xx, hh, ww;
	uint16 wtxt;
	uint32 fcolor, wcolor;
	uint8 c;
	uint16 ww_percent;
	datetime tval;
	ui_signal * signal = (ui_signal *)obj;
	uint16 percent;
	uint32 longtime;
	uint16 minute, hour;
	uint8 cbuf[10];
	int16 wbatt;
	int32 tmz;
	ui_object mold;
	uint32 filetime;
	net_context_p netctx;
	uint8 mold_w = 20;
	uint8 mold_h = 20;
	uint8 font_size = UI_FONT_DEFAULT;
	uint8 font_width = UI_FONT_DEFAULT_WIDTH;
	ui_resource * batt_list = (ui_resource *)g_image_batt_list20;
	ui_resource * wsig_list = (ui_resource *)g_image_wsig_list20;
	if(signal == NULL) goto exit_signal_render;
	if(signal->netctx == NULL) percent = 0;
	netctx = signal->netctx;
	if((netctx->state & IF_NET_STATE_CONNECTED) == 0) {
		percent = 0;
	} else {
		percent = 10;
		if(netctx->att > -60) percent = 33;
		if(netctx->att > -40) percent = 66;
		if(netctx->att > -20) percent = 90;
	}
	if_time_get((datetime_p)&tval);
	//fill background first
	if(obj->state != 0) {
		//generate background first
		ui_tab_render(obj, display);
	}
	if(((ui_rect *)signal)->h <= 40) {
		font_size = UI_FONT_SMALL;
		font_width = UI_FONT_SMALL_WIDTH;
		mold_w = 16;
		mold_h = 16;
		batt_list = (ui_resource *)g_image_batt_list16;
		wsig_list = (ui_resource *)g_image_wsig_list16;
		//goto skip_render_icon;
	}
	xx = (((w / 2) - mold_w) / 2);
	yy = (((h / 2) - mold_h) / 2);
	//render signal strength for Wi-Fi/GSM
	if(obj->state != 0 || percent != signal->percent || netctx->state != 0) {
		if(netctx->state & IF_NET_STATE_BUSY) {
			wcolor = (blink)?UI_COLOR_BLACK:UI_COLOR_LIME;
			blink = !blink;
		} else {
			wcolor = UI_COLOR_ORANGE;
		}
		signal->percent = percent;
		memset(&mold, 0, sizeof(ui_object));
		mold.backcolor = obj->backcolor;
		((ui_rect *)&mold)->w = mold_w;
		((ui_rect *)&mold)->h = mold_h;
		((ui_rect *)&mold)->y = y + yy;
		((ui_rect *)&mold)->x = x + xx;
		if(signal->netctx == NULL || (signal->netctx->state & (IF_NET_STATE_INITIALIZED | IF_NET_STATE_CONNECTED)) == 0) {
			ui_resource_render(display, (ui_rect *)&mold, &wsig_list[4], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
		} else {
			if(signal->percent > 80) 
				ui_resource_render(display, (ui_rect *)&mold, &wsig_list[0], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(signal->percent > 50)
				ui_resource_render(display, (ui_rect *)&mold, &wsig_list[1], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(signal->percent > 0)
				ui_resource_render(display, (ui_rect *)&mold, &wsig_list[2], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else 
				ui_resource_render(display, (ui_rect *)&mold, &wsig_list[3], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
		}
	}
	//skip_render_icon:
	//render battery
	wbatt = signal->batt;
	if(obj->state != 0) {		// || wbatt != signal->batt) {
		//signal->batt = wbatt;
		if(wbatt > 100) wbatt = 100;
		//generate background first
	}
	//calculate position
	xx = (w / 2) + (((w / 2) - mold_w) / 2);
	yy = (((h / 2) - mold_h) / 2);
	//render battery indicator
	if(if_pwr_is_charging() != signal->is_charging || obj->state != 0) {
		memset(&mold, 0, sizeof(ui_object));
		mold.backcolor = obj->backcolor;
		((ui_rect *)&mold)->w = mold_w;
		((ui_rect *)&mold)->h = mold_h;
		((ui_rect *)&mold)->y = y + yy;
		((ui_rect *)&mold)->x = x + xx;
		signal->is_charging = if_pwr_is_charging();
		if(signal->is_charging) {
			//ui_image_render(display, (ui_rect *)&mold, (uint8 *)g_image_png_batt_chrging, sizeof(g_image_png_batt_chrging), UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			ui_resource_render(display, (ui_rect *)&mold, &batt_list[0], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
		} else {
			if(wbatt > 80) 
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[1], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(wbatt > 60)
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[2], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(wbatt > 40)
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[3], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(wbatt > 20)
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[4], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else if(wbatt > 0)
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[5], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
			else
				ui_resource_render(display, (ui_rect *)&mold, &batt_list[6], UI_IMAGE_ALIGN_TOP | UI_IMAGE_ALIGN_LEFT);
		}
	}
	//calculate datetime with timezone
	filetime = tk_iso8601_to_filetime(&tval);
	//tmz = tval.tz * 3600;
	//filetime = (int32)filetime + (tval.tz * (int32)3600);
	tk_iso8601_from_filetime(&tval, filetime);
	//render date-time
	if(signal->hour != tval.hour || signal->min != tval.minute || obj->state != 0) {
		sprintf((char *)cbuf, "%02d:%02d", tval.hour, tval.minute);
		wtxt = strlen((const char *)cbuf) * font_width;
		xx = ((w - wtxt) >> 1);		//x = width - text_width / 2;
		yy = ((h - font_size)) - 2;		//y = height - text_height /2;
		display->fill_area(display, obj->backcolor, display->set_area(display, x+xx, y + yy, wtxt, font_size));
		display->print_string(display, font_size, x + xx, y + yy, (uint8 *)cbuf, UI_COLOR_WHITE); 
		signal->hour = tval.hour;
		signal->min = tval.minute;
	}
	//date-month
	if(signal->date != tval.date || signal->month != tval.month ||obj->state != 0) {
		if(font_size == UI_FONT_SMALL) sprintf((char *)cbuf, "%02d-%02d", tval.date, tval.month);
		else sprintf((char *)cbuf, "%02d %s", tval.date, gstr_months[tval.month]);
		wtxt = strlen((const char *)cbuf) * font_width;
		xx = ((w - wtxt) >> 1);		//x = width - text_width / 2;
		yy = ((h - (font_size * 2))) - 2;		//y = height - text_height /2;
		display->fill_area(display,  obj->backcolor, display->set_area(display, x+xx, y + yy, wtxt, font_size));
		display->print_string(display, font_size, x + xx, y + yy, (uint8 *)cbuf, UI_COLOR_WHITE); 
		signal->date = tval.date;
		signal->month = tval.month;
	}
	exit_signal_render:
	if(((ui_rect *)signal)->w >= 36) {
		ui_icon_draw_marker(obj, display);
	}
	OS_DEBUG_EXIT();
	return;
}

static void ui_ssidbtn_render(ui_object * obj, gui_handle_p display) {
	OS_DEBUG_ENTRY(ui_signal_render);
	uint16 yy, xx, hh, ww, tlen,t=0;
	int16 percent;
	int16 att;
	uint8 temp[20];
	uint16 x = ((ui_rect *)obj)->x;
	uint16 y = ((ui_rect *)obj)->y;
	uint16 w = ((ui_rect *)obj)->w;
	uint16 h = ((ui_rect *)obj)->h;
	ui_object rect;
	uint32 fcolor;
	att = ((ui_ssidbtn*)obj)->att;
	percent = 10;
	if(att > -120) percent = 25;
	if(att > -80) percent = 50;
	if(att > -40) percent = 75;
	
	if(obj->state & UI_STATE_INIT) {
		//display->fill_area(display, UI_COLOR_WHITE, display->set_area(display, x, y, w, h));
		//ui_tab_render(obj, display);
		ui_item_render(obj, display);
		display->set_area(display, x, y, w, h);
		xx = 28;
		yy = 4;
		tlen = strlen((const char *)((ui_ssidbtn *)obj)->ssid_name);
		if(tlen < 19) yy += 8;
		if(tlen < 38) yy += 4;
		for(t=0;t<tlen&&yy<(h-UI_FONT_DEFAULT);t+=19,yy+=UI_FONT_DEFAULT) {
			strncpy((char *)temp,  (const char *)((ui_ssidbtn *)obj)->ssid_name + t, 19);
			temp[19] = 0;
			display->print_string(display, UI_FONT_DEFAULT, x + xx, (y + yy), temp, UI_COLOR_WHITE);
		}
		//display security icon
		memcpy(&rect, obj, sizeof(ui_object));
		((ui_rect *)&rect)->x += 3;
		((ui_rect *)&rect)->y += 22;
		((ui_rect *)&rect)->w = 24;
		((ui_rect *)&rect)->h = 24;
		if(((ui_ssidbtn*)obj)->sec != 0) {
			ui_image_render(display, (ui_rect *)&rect, (uint8 *)image_png_lock16, sizeof(image_png_lock16), 0);
		} else {
			ui_image_render(display, (ui_rect *)&rect, (uint8 *)image_png_open16, sizeof(image_png_open16), 0);
		}
		//display separator border (bottom)
		//display->fill_area(display, UI_COLOR_RGB(30,31,42), display->set_area(display, x, y + h-4, w, 1));
		//display->fill_area(display, UI_COLOR_RGB(20,21,30), display->set_area(display, x, y + h-3, w, 1));
		display->fill_area(display, UI_COLOR_RGB(120,123,128), display->set_area(display, x, y + h-1, w, 1));
		//display->fill_area(display, UI_COLOR_RGB(60,62,90), display->set_area(display, x, y + h-1, w, 1));
	}
	if(obj->state & UI_STATE_INIT || percent != ((ui_ssidbtn *)obj)->percent) {
		((ui_ssidbtn *)obj)->percent = percent;
		xx = x + 3;
		yy = y + 15;
		hh = 3;
		ww = 4;
		if(((ui_ssidbtn *)obj)->percent > 0) fcolor = UI_COLOR_QUEEN_BLUE;
		else fcolor = UI_COLOR_BLACK;
		display->fill_area(display, fcolor, display->set_area(display, xx+1, yy+1, ww-2, hh-2));
		display->draw_rectangle(display, xx, yy, ww, hh, UI_COLOR_WHITE, 0);
		xx = x + 9;
		yy = y + 12;
		hh = 6;
		ww = 4;
		if(((ui_ssidbtn *)obj)->percent > 20) fcolor = UI_COLOR_QUEEN_BLUE;
		else fcolor = UI_COLOR_BLACK;
		display->fill_area(display, fcolor, display->set_area(display, xx + 1, yy + 1, ww-2, hh-2));
		display->draw_rectangle(display, xx, yy, ww, hh, UI_COLOR_WHITE, 0);
		xx = x + 15;
		yy = y + 9;
		hh = 9;
		ww = 4;
		if(((ui_ssidbtn *)obj)->percent > 40) fcolor = UI_COLOR_QUEEN_BLUE;
		else fcolor = UI_COLOR_BLACK;
		display->fill_area(display, fcolor, display->set_area(display, xx+1, yy+1, ww-2, hh-2));
		display->draw_rectangle(display, xx, yy, ww, hh, UI_COLOR_WHITE, 0);
		xx = x + 21;
		yy = y + 6;
		hh = 12;
		ww = 4;
		if(((ui_ssidbtn *)obj)->percent > 80) fcolor = UI_COLOR_QUEEN_BLUE;
		else fcolor = UI_COLOR_BLACK;
		display->fill_area(display, fcolor, display->set_area(display, xx+1, yy+1, ww-2, hh-2));
		display->draw_rectangle(display, xx, yy, ww, hh, UI_COLOR_WHITE, 0);
	}
	OS_DEBUG_EXIT();
}

