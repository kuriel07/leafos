ui_object * ui_tool_button_create(ui_rect * rect, ui_resource * normal_image, ui_resource * clicked_image, void (* click)(ui_object *, void *)) {
	ui_tool_button * obj = (ui_tool_button *)ui_create_text(sizeof(ui_tool_button), UI_TYPE_BUTTON | UI_ALIGN_FLOAT, (char *)"") ;
	if(obj != NULL) {
		((ui_object *)obj)->rect.x = rect->x;
		((ui_object *)obj)->rect.y = rect->y;
		((ui_object *)obj)->rect.w = rect->w;
		((ui_object *)obj)->rect.h = rect->h;
		((ui_object *)obj)->forecolor = UI_COLOR_WHITE;
		((ui_object *)obj)->backcolor = UI_COLOR_SLATE_GREY;
		((ui_object *)obj)->render = ui_tool_button_render;
		obj->normal_image.size = 0;
		obj->normal_image.buffer = NULL;
		obj->clicked_image.size = 0;
		obj->clicked_image.buffer = NULL;
		if(normal_image != NULL) memcpy(&obj->normal_image, normal_image, sizeof(ui_resource));
		if(clicked_image != NULL) memcpy(&obj->clicked_image, clicked_image, sizeof(ui_resource));
		((ui_object *)obj)->handler = click;
	}
	return (ui_object *)obj;
}

