static void ui_buttonset_click(ui_object * obj, void * display) {
	uint16 x = ((ui_rect *)obj)->x;
	uint16 y = ((ui_rect *)obj)->y;
	uint16 w = ((ui_rect *)obj)->w;
	uint16 h = ((ui_rect *)obj)->h;
	uint16 cx, cy;
	if_touch_get(display, &cx, &cy);
	
	if(cx > (x + (w / 2))) {			//navigation
		//right button
		((ui_buttonset *)obj)->button_state = UI_BUTTON_STATE_SUBMIT;
		if(((ui_buttonset *)obj)->submit != NULL) ((ui_buttonset *)obj)->submit(obj, display);
	} else {
		//left button
		((ui_buttonset *)obj)->button_state = UI_BUTTON_STATE_CANCEL;
		if(((ui_buttonset *)obj)->cancel != NULL) ((ui_buttonset *)obj)->cancel(obj, display);
	}
}

ui_object * ui_buttonset_create(uint16 type, uint8 id, void (* submit)(ui_object *, void *), void (* cancel)(ui_object *, void *)) {
	ui_buttonset * obj = (ui_buttonset *)ui_create_text(sizeof(ui_buttonset), UI_TYPE_BUTTON | UI_ALIGN_VERTICAL, (char *)"") ;
	((ui_object *)obj)->render = ui_buttonset_render;
	((ui_object *)obj)->rect.h = 48;
	((ui_object *)obj)->forecolor = UI_COLOR_WHITE;
	((ui_object *)obj)->backcolor = UI_COLOR_SLATE_GREY;
	((ui_button *)obj)->style = UI_BUTTON_STYLE_NORMAL;	//UI_BUTTON_STYLE_NORMAL;
	((ui_button *)obj)->id = id;
	((ui_object *)obj)->handler = ui_buttonset_click;
	obj->type = type;
	obj->submit = submit;
	obj->cancel = cancel;
	return (ui_object *)obj;
}

