void ui_icon_draw_marker(ui_object * obj, gui_handle_p display) {
	uint16 x = ((ui_rect *)obj)->x;
	uint16 y = ((ui_rect *)obj)->y;
	uint16 w = ((ui_rect *)obj)->w;
	uint16 h = ((ui_rect *)obj)->h;
	uint32 fcolor = UI_COLOR_BLUE;
	if(((ui_icon_tool *)obj)->show_marker) {
		fcolor = UI_COLOR_BLUE;
	} else {
		fcolor = obj->backcolor;
	}
	if(display->orientation & 0x01) {
		display->fill_area(display, fcolor, display->set_area(display, x, y, w, 2));
		//borders
		display->fill_area(display, UI_COLOR_RGB(49,51,59), display->set_area(display, x, y, 1, h));
		display->fill_area(display, UI_COLOR_RGB(79,81,89), display->set_area(display, (x + w) - 1, y, 1, h));
	} else {
		display->fill_area(display, fcolor, display->set_area(display, (x + w) - 2, y, 2, h));
		//borders
		display->fill_area(display, UI_COLOR_RGB(49,51,59), display->set_area(display, x, y, w, 1));
		display->fill_area(display, UI_COLOR_RGB(79,81,89), display->set_area(display, x, (y + h) - 1, w, 1));
	}
}

