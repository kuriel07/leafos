	ui_icon_tool * obj = (ui_icon_tool *)ui_create_text(sizeof(ui_icon_tool), UI_TYPE_GRIDITEM | UI_ALIGN_GRID, (char *)text) ;
	((ui_object *)obj)->rect.w = width;
	((ui_object *)obj)->rect.h = height;
	strcpy((char *)((ui_object *)obj)->name, (const char *)name);
	((ui_object *)obj)->render = ui_icon_tool_render;
